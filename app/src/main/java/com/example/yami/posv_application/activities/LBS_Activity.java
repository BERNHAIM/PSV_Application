    /* 위험 지역 반경에 들어가면 푸시알람 */
    public void pushNearDangerArea(LatLng latLng){
        double distance;

        JSONObject position = null;
        JSONArray positions = null;
        String locations = loadJSONFromAsset();

        // 위험지역 좌표를 저장하는 arraylist
        ArrayList<double[]> dangerAreaList = new ArrayList<double[]>();

        Location start = new Location("Start");
        Location destination = new Location("Destination");

        try {
            position = new JSONObject(locations);
            positions = position.getJSONArray("location");

            for (int i = 0; i < positions.length(); i++) {
                double x = positions.getJSONObject(i).getDouble("x");
                double y = positions.getJSONObject(i).getDouble("y");
                // x  : Latitude , y : Longitude
                double[] desinations = {x,y};

                dangerAreaList.add(desinations);

                Log.d("destination"+i,Arrays.toString(dangerAreaList.get(i)));

                /*
                 * 이제 각 위험지역과 내 현재 위치를 비교해서 1000m(반경)안인 경우
                 * push알람을 보내주어 경고를 보내주게끔 만들어보자.
                 */
                //lat/lng: (37.3059057,127.0043627)
                start.setLatitude(latLng.latitude);
                start.setLongitude(latLng.longitude);

                destination.setLatitude(x);
                destination.setLongitude(y);

                distance = start.distanceTo(destination);
                Log.d("Distance : ",Double.toString(distance));

                if(distance <= 1000){
                    NotificationManaging();
                }



            }
        }catch (Exception e){
            e.printStackTrace();
        }

    }

